
function main()
	
	-- Exemplu 1

	-- aici numara din 2 in 2 
	for aici_pui_orice_nume_vrei_tu = 1, 6, 2 do 
		print(aici_pui_orice_nume_vrei_tu)
	end
	
	-- output:
	-- 1
	-- 3
	-- 5

	-- Exemplu 2 
	
	-- aici numara din 5 in 5 pana la 20 
	for orice_nume = 0, 20, 5 do 
		print(orice_nume)
	end

	-- Output:
	-- 0
	-- 5
	-- 10
	-- 15
	-- 20

	-- Exemplu 3

	-- aici numara descrescator de la 6 spre 1 din 1 in 1
	for orice = 6,1,-1 do 
		print(orice)
	end

	-- output:
	-- 6
	-- 5
	-- 4
	-- 3
	-- 2
	-- 1
	
end

