
--  verifica daca prima valoare, e mai mica decat cea dea doua

function main()

    -- daca 1 e mai mic decat 2 atunci
    if (1 < 2) then
        print("Da, 1 e mai mic decat  2")
    else
        print("Nu, 1 nu este mai mic decat 2")
    end
    -- Output: Da, 1 e mai mic decat  2

    if (3 < 2) then
        print("Da, 3 e mai mic decat  2")
    else
        print("Nu, 3 nu este mai mic decat 2")
    end
    -- Output: Nu, 3 nu este mai mic decat 2

end

