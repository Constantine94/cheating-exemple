-- While loop este folosit pentru a repeta diverse lucruri de un numar nestiut de ori

function main()

    -- Exemplu 1

    while true do  -- cat time expresia e adevarata, va executa asta la infinit, while cauta sa vada daca e adevarata expresia
        print("Voi printa chestia asta la infinit")
    end

    -- Output: Va printa fara sa se opreasca print()-ul de mai sus - Voi printa chestia asta la infinit


    -- Exemple 2

    b = 10
    while b > 9 do -- b e mai mare decat 9, pentru ca 10 e mai mare decat 9. Expresia e adevarata, deci va printa la infinit "Voi printa asta la infinit"
        print("Voi prin asta la infinit")
    end

    -- Output:  Va printa la infinit "Voi printa asta la infinit"

end
