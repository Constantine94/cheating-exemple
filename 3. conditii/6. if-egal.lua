
--  == verifica daca 2 valori sunt egale

function main()

        -- daca 0 e egal cu 0 atunci
        if (0 == 0) then
            print("Da, 0 este egal 0")
        else
            print("Nu, 0 nu este egal cu 0")
        end
        -- Output: Da, 0 este egal 0
    

        if (1 == 0) then
            print("Da, 1 este egal 0")
        else
            print("Nu, 1 nu este egal cu 0")
        end
        -- Output: Nu, 1 nu este egal cu 0
        
end

