
-- ~= verifica daca 2 valori nu sunt egale

function main()

    
    --  daca 1 nu e egal cu 0 atunci
    if (1 ~= 0) then
        print("Da, 1 nu este egal 0")
    else
        print("Nu, 1 este egal cu 0")
    end
    -- Output: Da, 1 nu este egal 0


    if (1 ~= 1) then
        print("Da, 1 nu este egal 1")
    else
        print("Nu, 1 este egal cu 1")
    end
    -- Output: Nu, 1 este egal cu 1

end

